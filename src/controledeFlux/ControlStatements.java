package controledeFlux;

public class ControlStatements {

	public static void main(String[] args) {
		String a="spanish";
				if (a=="french") {
					System.out.println("bonjour");
				} else if (a=="english") {
					System.out.println("hello");
				} else if (a=="portugese") {
					System.out.println("bom dia");
				} else if (a=="spanish") {
					System.out.println("buenos dias");
				} else {
					System.out.println(":)");
				}
				
	}

}
